def parse_fn(x):
  ret = []
  stop = False
  for line in x.split('\n'):
    if line:
      ret.append(line)
      if '[[' in line:
        stop = False
      elif ']]' in line:
        stop = True
      if stop:
        txt = '\n'.join(ret)
        yield txt
        del ret[:]

def write_fn(x, prefix='', postfix='.txt', count=0):
  for i, txt in enumerate(x):
    fname = '{}{}{}'.format(prefix, count + i, postfix)
    with open(fname, 'w') as f:
      f.write(txt)

